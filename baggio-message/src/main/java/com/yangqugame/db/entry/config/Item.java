package com.yangqugame.db.entry.config;


public class Item {

  public int id;
  public String itemname;
  public int ascription;
  public int amountlimit;
  public boolean automaticuse;
  public int usetype;
  public String useparameter;
  public String openmoudel;
  public String getway;
  public int page;
  public String pic;
  public String description;


  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }


  public String getItemname() {
    return itemname;
  }

  public void setItemname(String itemname) {
    this.itemname = itemname;
  }


  public int getAscription() {
    return ascription;
  }

  public void setAscription(int ascription) {
    this.ascription = ascription;
  }


  public int getAmountlimit() {
    return amountlimit;
  }

  public void setAmountlimit(int amountlimit) {
    this.amountlimit = amountlimit;
  }


  public boolean getAutomaticuse() {
    return automaticuse;
  }

  public void setAutomaticuse(boolean automaticuse) {
    this.automaticuse = automaticuse;
  }


  public int getUsetype() {
    return usetype;
  }

  public void setUsetype(int usetype) {
    this.usetype = usetype;
  }


  public String getUseparameter() {
    return useparameter;
  }

  public void setUseparameter(String useparameter) {
    this.useparameter = useparameter;
  }


  public String getOpenmoudel() {
    return openmoudel;
  }

  public void setOpenmoudel(String openmoudel) {
    this.openmoudel = openmoudel;
  }


  public String getGetway() {
    return getway;
  }

  public void setGetway(String getway) {
    this.getway = getway;
  }


  public int getPage() {
    return page;
  }

  public void setPage(int page) {
    this.page = page;
  }


  public String getPic() {
    return pic;
  }

  public void setPic(String pic) {
    this.pic = pic;
  }


  public String getDescription() {
    return description;
  }

  public void setDescription(String description) {
    this.description = description;
  }

}
