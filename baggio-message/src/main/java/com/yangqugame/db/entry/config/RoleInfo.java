package com.yangqugame.db.entry.config;


public class RoleInfo {

  public int roletype;
  public String name;
  public String iscommon;
  public String bornstar;
  public String feature;
  public String gender;
  public String introduction;
  public String commonfragment;
  public String fragmentsource;


  public int getRoletype() {
    return roletype;
  }

  public void setRoletype(int roletype) {
    this.roletype = roletype;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getIscommon() {
    return iscommon;
  }

  public void setIscommon(String iscommon) {
    this.iscommon = iscommon;
  }


  public String getBornstar() {
    return bornstar;
  }

  public void setBornstar(String bornstar) {
    this.bornstar = bornstar;
  }


  public String getFeature() {
    return feature;
  }

  public void setFeature(String feature) {
    this.feature = feature;
  }


  public String getGender() {
    return gender;
  }

  public void setGender(String gender) {
    this.gender = gender;
  }


  public String getIntroduction() {
    return introduction;
  }

  public void setIntroduction(String introduction) {
    this.introduction = introduction;
  }


  public String getCommonfragment() {
    return commonfragment;
  }

  public void setCommonfragment(String commonfragment) {
    this.commonfragment = commonfragment;
  }


  public String getFragmentsource() {
    return fragmentsource;
  }

  public void setFragmentsource(String fragmentsource) {
    this.fragmentsource = fragmentsource;
  }

}
